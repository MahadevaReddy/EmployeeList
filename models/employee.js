class Employee {
  constructor(id, employerId, title, imageUrl, description, salary, emailId, mobile) {
    this.id = id;
    this.employerId = employerId;
    this.imageUrl = imageUrl;
    this.title = title;
    this.description = description;
    this.salary = salary;
    this.emailId = emailId;
    this.mobile = mobile;
  }
}

export default Employee;
